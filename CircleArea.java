// note that double is used to store floating point numbers - it stands for double precision
// The area of a circle is calculated using the formula: area = π * r^2
// where π is a constant approximately equal to 3.14159 and r is the radius of the circle

class CircleArea {
    public static void main(String[] args) {
        double radius = 4.0;
        double area = 3.14 * radius * radius;
        System.out.println("The area of the circle is " + area);
    }
}
